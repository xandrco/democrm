    public function run(): void
    {
        $users = User::all();
        
        $statuses = ['pending', 'approved', 'rejected'];
        
        for ($i = 0; $i < count($this->applications); $i++) {
            $appData = $this->applications[$i];

            $status = $statuses[array_rand($statuses)];
            
            $reviewedBy = null;
            $reviewedAt = null;
            
            if ($status !== 'pending') {
                $reviewedBy = $users->random()->id;
                $reviewedAt = Carbon::now()->subDays(rand(1, 30));
            }
            
            $metadata = [
                'ip_address' => '192.168.' . rand(0, 255) . '.' . rand(0, 255),
                'browser' => ['Chrome', 'Firefox', 'Safari', 'Edge'][array_rand(['Chrome', 'Firefox', 'Safari', 'Edge'])],
                'referrer' => ['Google', 'Facebook', 'Twitter', 'Direct'][array_rand(['Google', 'Facebook', 'Twitter', 'Direct'])],
            ];
            
            Application::create([
                'name' => $appData['name'],
                'email' => $appData['email'],
                'message' => $appData['message'],
                'status' => $status,
                'reviewed_by' => $reviewedBy,
                'reviewed_at' => $reviewedAt,
                'metadata' => $metadata,
                'created_at' => Carbon::now()->subDays(rand(1, 60)),
                'updated_at' => Carbon::now()->subDays(rand(0, 30)),
            ]);
        }
    }
